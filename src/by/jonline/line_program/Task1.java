package by.jonline.line_program;

public class Task1 {
    /*
     * 1. Найдите значение функции: z = ( (a – 3 ) * b / 2) + c
     */
    public static void main(String[] args) {
        double a = 2.0;
        double b = 3.2;
        double c = 5.2;

        double z = ((a - 3.0) * b / 2.0) + c; // z = 3.6
        System.out.print("Line program. Task 1. Result: " + z);
    }

}
